for i, item in enumerate(data, 1):
    # 提取型号规格 - 使用 \s* 来匹配可能的换行和空格
    match = re.search(r"型号规格\s*[:：]\s*(.+)", item)
    if match:
        raw_model = match.group(1).strip()
        try:
            normalized = normalize(raw_model)
            print(f"{i:2}. {raw_model:35} -> {normalized}")
        except Exception as e:
            print(f"{i:2}. {raw_model:35} -> ❌ 错误：{e}")
    else:
        print(f"{i:2}. 未找到型号规格")

